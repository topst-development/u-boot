void tcc_dphy_setup_pll(struct mipi_dsi_dev *dev)
{
	unsigned int rsel = 0;
	unsigned int sscg_en = 0;
	unsigned int fsel = 0;
	unsigned int bypass = 0;
	unsigned int afc_enb = 0;
	unsigned int extafc = 0;
	unsigned int reset_n_sel = 1;
	unsigned int pll_en_sel = 1;
	unsigned int icp = 0;
	unsigned int sel_pf = 0;

	unsigned long apb_data;

	unsigned int hz = dev->data_rate;
	unsigned int phylist_size;
	unsigned int i;
	struct dphy_pmsk_mfr_mrr_table *pll_pmsk_t = NULL;

	phylist_size =
		sizeof(tcc_dsi_dphy_configs) / sizeof(struct dsi_dphy_configs);

	for(i = 0 ; i < phylist_size ; i++) {
		if(hz == tcc_dsi_dphy_configs[i].hz) {
			pll_pmsk_t = &tcc_dsi_dphy_configs[i].pmsk;
			pr_err("%s found pll for %d\n", __func__, hz);
			break;
		}
	}
	if(pll_pmsk_t == NULL) {
		pr_err("%s can't find pms value with %dHz\n", __func__, hz);
		return;
	}


	apb_data = 0;
	apb_data |= (pll_pmsk_t->s <<PLL_S_SHIFT | pll_pmsk_t->p <<PLL_P_SHIFT );
	mipi_dsih_dphy_write_word(dev, PLL_CON0_OFFSET, apb_data);
		//mipi_dsih_dphy_write_part(dev, PLL_CON0_OFFSET, s, PLL_S_SHIFT, 3);
		//mipi_dsih_dphy_write_part(dev, PLL_CON0_OFFSET, p, PLL_P_SHIFT, 6);
	apb_data = 0;
	apb_data |= (pll_pmsk_t->k << PLL_K_SHIFT);
	mipi_dsih_dphy_write_word(dev, PLL_CON1_OFFSET, apb_data);
	
	apb_data = 0;
	apb_data |= (pll_pmsk_t->m << PLL_M_SHIFT);
	mipi_dsih_dphy_write_word(dev, PLL_CON2_OFFSET, apb_data);
		// mipi_dsih_dphy_write_part(dev, PLL_CON1_OFFSET, k, PLL_K_SHIFT, 16);
		// mipi_dsih_dphy_write_part(dev, PLL_CON2_OFFSET, fout_mask, PLL_FOUT_MASK_SHIFT, 1);
		// mipi_dsih_dphy_write_part(dev, PLL_CON2_OFFSET, feed_en, FEED_EN_SHIFT, 1);
		// mipi_dsih_dphy_write_part(dev, PLL_CON2_OFFSET, m, PLL_M_SHIFT, 10);
	apb_data = 0;
	apb_data |= (pll_pmsk_t->mrr << MRR_SHIFT | pll_pmsk_t->mfr << MFR_SHIFT );
	mipi_dsih_dphy_write_word(dev, PLL_CON3_OFFSET, apb_data);

	mipi_dsih_dphy_write_part(dev, PLL_CON4_OFFSET, rsel, RSEL_SHIFT, 4);
	mipi_dsih_dphy_write_part(dev, PLL_CON4_OFFSET, sscg_en, SSCG_EN_SHIFT, 1);
	mipi_dsih_dphy_write_part(dev, PLL_CON4_OFFSET, fsel, FSEL_SHIFT, 1);
	mipi_dsih_dphy_write_part(dev, PLL_CON4_OFFSET, bypass, BYPASS_SHIFT, 1);
	mipi_dsih_dphy_write_part(dev, PLL_CON4_OFFSET, afc_enb, AFC_ENB_SHIFT, 3);
	mipi_dsih_dphy_write_part(dev, PLL_CON4_OFFSET, extafc, EXTAFC_SHIFT, 5);

	mipi_dsih_dphy_write_part(dev, PLL_CON5_OFFSET, reset_n_sel, RESET_N_SEL_SHIFT, 1);
	mipi_dsih_dphy_write_part(dev, PLL_CON5_OFFSET, pll_en_sel, PLL_EN_SEL_SHIFT, 1);
	mipi_dsih_dphy_write_part(dev, PLL_CON5_OFFSET, icp, ICP_SHIFT, 2);
	mipi_dsih_dphy_write_part(dev, PLL_CON5_OFFSET, sel_pf, SEL_PF_SHIFT, 2);

	mipi_dsih_dphy_write_part(dev, PLL_CON6_OFFSET, 0x6, WCKJ_BUF_SFT_CNT_SHIFT, 4);

	mipi_dsih_dphy_write_word(dev, PLL_CON7_OFFSET, 0x00001450);
	mipi_dsih_dphy_write_word(dev, PLL_CON8_OFFSET, 0x00000A28);

}

void tcc_dphy_set_master_clk_lane(struct mipi_dsi_dev *dev)
{
	unsigned int hz = dev->data_rate;
	unsigned int phylist_size;
	unsigned int i;
	struct dphy_master_clock_timing *mc_timing_t = NULL;

	phylist_size =
		sizeof(tcc_dsi_dphy_configs) / sizeof(struct dsi_dphy_configs);

	for(i = 0 ; i < phylist_size ; i++) {
		if(hz == tcc_dsi_dphy_configs[i].hz) {
			mc_timing_t = &tcc_dsi_dphy_configs[i].mc_lane;
			pr_info("%s found mc lane timing for %u\n", __func__, hz);
			break;
		}
	}
	if(mc_timing_t == NULL) {
		pr_err("%s can't find pms value with %dHz\n", __func__, hz);
		return;
	}

	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON0_OFFSET, mc_timing_t->hstx_clk_sel, HSTX_CLK_SEL_SHIFT, 1);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON0_OFFSET, mc_timing_t->t_lpx, T_LPX_SHIFT, 8);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON1_OFFSET, mc_timing_t->t_clk_zero, T_CLK_ZERO_SHIFT, 8);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON1_OFFSET, mc_timing_t->t_clk_prepare, T_CLK_PREPARE_SHIFT, 8);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON2_OFFSET, mc_timing_t->t_hs_exit, T_HS_EXIT_SHIFT, 8);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON2_OFFSET, mc_timing_t->t_clk_trail, T_CLK_TRAIL_SHIFT, 8);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON3_OFFSET, mc_timing_t->t_clk_post, T_CLK_POST_SHIFT, 8);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_TIME_CON4_OFFSET, mc_timing_t->t_ulps_exit, T_ULPS_EXIT_SHIFT, 10);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_DESKEW_OFFSET, mc_timing_t->skew_cal_run_time, SKEW_CAL_RUN_TIME_SHIFT, 4);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_DESKEW_OFFSET, mc_timing_t->skew_cal_init_run_time, SKEW_CAL_INIT_RUN_TIME_SHIFT, 4);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_DESKEW_OFFSET, mc_timing_t->skew_cal_init_wait_time, SKEW_CAL_INIT_WAIT_TIME_SHIFT, 4);
	mipi_dsih_dphy_write_part(dev, MASTER0_CLK_DESKEW_OFFSET, mc_timing_t->skew_cal_en, SKEW_CAL_EN_SHIFT, 1);
}

