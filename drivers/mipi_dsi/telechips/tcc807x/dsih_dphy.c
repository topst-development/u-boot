void tcc_dsi_phy_init(struct mipi_dsi_dev *dev)
{
    unsigned int index;
	unsigned int value;
  //------------------------------------------------------------------------
  // Block      Instance Name               Base Address
  //------------------------------------------------------------------------
  // BIAS       BIAS                           0x0000
  // PLL        PLL                            0x0100
  //
  // MC         Master Clock Lane              0x0300
  // CMD        Master Data0 Combo             0x0400
  // CMD        Master Data1 Combo             0x0500
  // CMD        Master Data2 Combo             0x0600
  // MD         Master Data3 D-PHY             0x0700
  //
  // SC         Slave 0 Clock Lane             0x0B00
  // CSD        Slave 0 Data0 Combo            0x0C00
  // CSD        Slave 0 Data1 Combo            0x0D00
  // CSD        Slave 0 Data2 Combo            0x0E00
  // SD         Slave 0 Data3 D-PHY            0x0F00
  //------------------------------------------------------------------------
    // Set BIAS As default
    mipi_dsih_dphy_write_word(dev, 0x0000, 0x00000010 ); //
    mipi_dsih_dphy_write_word(dev, 0x0004, 0x00000110 ); //
    mipi_dsih_dphy_write_word(dev, 0x0008, 0x00003223 ); //
    mipi_dsih_dphy_write_word(dev, 0x000C, 0x00000000 ); //
    mipi_dsih_dphy_write_word(dev, 0x0010, 0x00000000 ); //

    // Set - Serch PLL
    tcc_dphy_setup_pll(dev);
    udelay(164);
    // Set Master Clock , Data lane
    mipi_dsih_dphy_write_word(dev, 0x0304, 0x00001450 ); //
    mipi_dsih_dphy_write_word(dev, 0x0308, 0x00007133 ); //
    mipi_dsih_dphy_write_word(dev, 0x030C, 0x00000000 ); //[1:0] EMP above 4500Mbps
    udelay(210);
    tcc_dphy_set_master_clk_lane(dev);
    tcc_dphy_set_master_data_lane(dev);
    udelay(164);

    // Set PLL EN
    mipi_dsih_dphy_write_part(dev, PLL_CON0_OFFSET, 1, PLL_EN_SHIFT, 1);
    udelay(211);
    // Set PHY Enable
    for(index = 0 ; index <= dev->phy_cfg.phy_lanes ; index ++) {
        mipi_dsih_dphy_write_part(dev, MASTER0_CLK_GNR_CON0_OFFSET + (index * 0x100), 0x1, 0, 1);
    }
    udelay(210);

	// CAM sub + 0x164 
	// [0]  PHY 0 - check 1
	// [16] PHY 1 - check 1
	do{
		value = __raw_readl(0x1c004164);
		if(dev->port == 0)
			value &= 0x1;
		else
			value &= 0x10000;
		// pr_err("0x%x\n", value);
	}while(!value);
	pr_err("PHY0 LOCK CNT Checked\n");

    // Check PLL Ready
	for(index = 0 ; index <= dev->phy_cfg.phy_lanes - 1 ; index ++) {
		do	{
			value = mipi_dsih_dphy_read_word(dev, 0x300 + (index * 0x100));
			value &= 0x2;
		} while (!value);
		pr_err("Lane %d PLL LOCK Checked\n", index);
	}

    pr_info("Release Reset\n");
    __raw_writel(0x20000 , 0x1c004000);

	if(dev->data_rate >= 1500) { // calibration
		pr_info("SkewCalSet\n");
		mipi_dsih_dphy_write_part(dev, MASTER0_CLK_DESKEW_OFFSET, 1, SKEW_CAL_EN_SHIFT, 1);
	}

	udelay(40);
}
