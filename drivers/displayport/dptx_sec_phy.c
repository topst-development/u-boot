int32_t Dptx_Sec_PHY_Init(struct Dptx_Params *pstDptx, uint8_t ucLinkRate, uint8_t ucLanes)
{
	bool bOffsetInRange, bPll_Ready, bPhy_Ready;
	uint8_t uclink_rate;
	int32_t iRetVal = DPTX_RETURN_NO_ERROR;
	uint32_t uiElements, uiPhy_Cfg;
	uint32_t uiReg_Addr, uiReg_Offset, uiReg_W_data;
	struct SEC_PHY_Reg_Data *pstLink_Reg_Data;
	struct SEC_PHY_Reg_Data *pstAux_Reg_Data;
	struct SEC_PHY_Reg_Data *pstSB_Reg_Data;

	if (pstDptx->ePhy_Dev != PHY_DEVICE_SEC) {
		dptx_dbg("It's valid for SEC Phy only.. phy type = %d", pstDptx->ePhy_Dev);

		iRetVal = DPTX_RETURN_EINVAL;

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	bOffsetInRange = CHECK_REG_OFFSET(pstDptx->uiSEC_PHY_Reg_Offset);
	if (!bOffsetInRange) {
		dptx_err("Invalid reg offset as 0x%x", pstDptx->uiRegBank_RegAddr_Offset);

		iRetVal = DPTX_RETURN_EINVAL;

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	dptx_info("Init SEC Phy with Max rate(%u) = %s",
				ucLinkRate,
				(ucLinkRate == (uint8_t)DPTX_PHYIF_CTRL_RATE_RBR) ? "RBR" :
				(ucLinkRate == (uint8_t)DPTX_PHYIF_CTRL_RATE_HBR) ? "HBR" :
				(ucLinkRate == (uint8_t)DPTX_PHYIF_CTRL_RATE_HBR2) ? "HBR2" :"HBR3");

	uiReg_Offset = pstDptx->uiSEC_PHY_Reg_Offset;

	pstLink_Reg_Data = stSEC_PHY_Link_Reg_Data;

	uiPhy_Cfg = (ucLinkRate == (uint8_t)DPTX_PHYIF_CTRL_RATE_HBR3) ? (uint32_t)(PHY_CLK_DIV_DISABLE | RO_REF_RO_PLL) : (uint32_t)(PHY_CLK_DIV_DISABLE | LC_REF_RO_PLL);

	iRetVal = Dptx_Cfg_Set_PHY_Cfg(pstDptx, uiPhy_Cfg);

	for (uiElements = 0; (pstLink_Reg_Data[uiElements].uireg_add != (uint32_t)SEC_PHY_REG_MAX); uiElements++) {

		uclink_rate = pstLink_Reg_Data[uiElements].uclink_rate;

		if ((uclink_rate != LINK_RATE_MAX) && (uclink_rate != ucLinkRate)) {
			/*For KCS */
			continue;
		}

		if (pstLink_Reg_Data[uiElements].uireg_add == (uint32_t)SEC_PHY_DELAY_USEC_REG) {
			udelay(pstLink_Reg_Data[uiElements].uireg_val);
			continue;
		}

		//dptx_info("Reg[0x%x] = 0x%x", pstLink_Reg_Data[uiElements].uireg_add, pstLink_Reg_Data[uiElements].uireg_val);

		uiReg_W_data = pstLink_Reg_Data[uiElements].uireg_val;
		uiReg_Addr = pstLink_Reg_Data[uiElements].uireg_add;

		Dptx_Reg_Writel(pstDptx, (uiReg_Offset + uiReg_Addr), uiReg_W_data);
	}

	uiPhy_Cfg |= (uint32_t)(CFG_BIAS_EN | CFG_BGR_EN);

	iRetVal = Dptx_Cfg_Set_PHY_Cfg(pstDptx, uiPhy_Cfg);

	pstAux_Reg_Data = stSEC_PHY_Aux_Reg_Data;

	for (uiElements = 0; (pstAux_Reg_Data[uiElements].uireg_add != (uint32_t)SEC_PHY_REG_MAX); uiElements++) {

		uclink_rate = pstAux_Reg_Data[uiElements].uclink_rate;

		if ((uclink_rate != LINK_RATE_MAX) && (uclink_rate != ucLinkRate)) {
			/*For KCS */
			continue;
		}

		if (pstAux_Reg_Data[uiElements].uireg_add == (uint32_t)SEC_PHY_DELAY_USEC_REG) {
			udelay(pstAux_Reg_Data[uiElements].uireg_val);
			continue;
		}

//		dptx_info("Reg[0x%x] = 0x%x", pstAux_Reg_Data[uiElements].uireg_add, pstAux_Reg_Data[uiElements].uireg_val);

		uiReg_W_data = pstAux_Reg_Data[uiElements].uireg_val;
		uiReg_Addr = pstAux_Reg_Data[uiElements].uireg_add;

		Dptx_Reg_Writel(pstDptx, (uiReg_Offset + uiReg_Addr), uiReg_W_data);
	}

	Dptx_Cfg_SoftReset(pstDptx, (uint32_t)(CFG_LANE_RESET | CFG_CMN_RESET | CFG_DP_X2X_SLV_RESET));

	udelay(10);

	uiPhy_Cfg |= (uint32_t)CFG_PLL_EN;
	iRetVal = Dptx_Cfg_Set_PHY_Cfg(pstDptx, uiPhy_Cfg);

	udelay(10);

	Dptx_Cfg_SoftReset(pstDptx, (uint32_t)(CFG_LANE_RESET |     CFG_DP_X2X_SLV_RESET));

	udelay(1000);

	iRetVal = Dptx_Cfg_Check_PHY_Pll(pstDptx, &bPll_Ready);
	if (iRetVal != DPTX_RETURN_NO_ERROR) {
		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	if (!bPll_Ready) {
		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	Dptx_Cfg_SoftReset(pstDptx, (uint32_t)CFG_DP_X2X_SLV_RESET);

	iRetVal = Dptx_Cfg_Check_PHY_Ready(pstDptx, &bPhy_Ready);
	if (iRetVal != DPTX_RETURN_NO_ERROR) {
		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	if (!bPll_Ready) {
		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	pstSB_Reg_Data = stSEC_PHY_SB_Reg_Data;

	for (uiElements = 0; (pstSB_Reg_Data[uiElements].uireg_add != (uint32_t)SEC_PHY_REG_MAX); uiElements++) {

		uclink_rate = pstSB_Reg_Data[uiElements].uclink_rate;

		if ((uclink_rate != LINK_RATE_MAX) && (uclink_rate != ucLinkRate)) {
			/*For KCS */
			continue;
		}

		if (pstSB_Reg_Data[uiElements].uireg_add == (uint32_t)SEC_PHY_DELAY_USEC_REG) {
			udelay(pstSB_Reg_Data[uiElements].uireg_val);
//			dptx_info(" 0x%x : %u", pstSB_Reg_Data[uiElements].uireg_add, pstSB_Reg_Data[uiElements].uireg_val);
			continue;
		}

//		dptx_info("Reg[0x%x] = 0x%x", pstSB_Reg_Data[uiElements].uireg_add, pstSB_Reg_Data[uiElements].uireg_val);

		uiReg_W_data = pstSB_Reg_Data[uiElements].uireg_val;
		uiReg_Addr = pstSB_Reg_Data[uiElements].uireg_add;

		Dptx_Reg_Writel(pstDptx, (uiReg_Offset + uiReg_Addr), uiReg_W_data);
	}

	iRetVal = Dptx_Cfg_Check_Sec_PHY_SB_Ready(pstDptx, &bPhy_Ready);
	if (iRetVal != DPTX_RETURN_NO_ERROR) {
		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	iRetVal = Dptx_Sec_Set_NumOfLanes(pstDptx, ucLanes);

return_funcs:
	return iRetVal;
}

int32_t Dptx_Sec_Set_NumOfLanes(struct Dptx_Params *pstDptx, uint8_t ucLanes)
{
	bool bOffsetInRange;
	uint32_t uiReg_Addr, uiReg_Offset, uiReg_W_data;
	int32_t iRetVal = DPTX_RETURN_NO_ERROR;

	bOffsetInRange = CHECK_REG_OFFSET(pstDptx->uiSEC_PHY_Reg_Offset);
	if (!bOffsetInRange) {
		dptx_err("Invalid reg offset as 0x%x", pstDptx->uiRegBank_RegAddr_Offset);

		iRetVal = DPTX_RETURN_EINVAL;

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	uiReg_Offset = pstDptx->uiSEC_PHY_Reg_Offset;
	uiReg_Addr = (uint32_t)SEC_PHY_REG0207;

	switch (ucLanes) {
	case PHY_LANE_1:
		uiReg_W_data = 0x01U;
		break;
	case PHY_LANE_2:
		uiReg_W_data = 0x03U;
		break;
	case PHY_LANE_4:
		uiReg_W_data = 0x0FU;
		break;
	default:
		dptx_err("Invalid Num of lanes as 0x%x", ucLanes);
		break;
	}

	Dptx_Reg_Writel(pstDptx, (uiReg_Offset + uiReg_Addr), uiReg_W_data);

return_funcs:
	return iRetVal;
}

int32_t Dptx_Sec_Set_EQ(struct Dptx_Params *pstDptx, uint8_t ucLane_Idx, uint8_t ucLink_Rate, uint8_t ucVsw, uint8_t ucPreemp)
{
	bool bOffsetInRange;
	uint8_t ucEQ_Case;
	int32_t iRetVal = DPTX_RETURN_NO_ERROR;
	uint32_t uiReg_Offset, uiReg_Addr, uiReg_W_data;

	if (pstDptx->ePhy_Dev != PHY_DEVICE_SEC) {
		dptx_dbg("Nothing to do not for Samsung PHY");

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	if (ucVsw >= (uint8_t)VOLTAGE_SWING_LEVEL_MAX) {
		dptx_err("Invalid VSW as %u", ucVsw);
		iRetVal = DPTX_RETURN_EINVAL;

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	if (ucPreemp >= (uint8_t)PRE_EMPHASIS_LEVEL_MAX) {
		dptx_err("Invalid Pre-emp as %u", ucPreemp);
		iRetVal = DPTX_RETURN_EINVAL;

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	bOffsetInRange = CHECK_REG_OFFSET(pstDptx->uiSEC_PHY_Reg_Offset);
	if (!bOffsetInRange) {
		dptx_err("Invalid reg offset as 0x%x", pstDptx->uiRegBank_RegAddr_Offset);

		iRetVal = DPTX_RETURN_EINVAL;

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	uiReg_Offset = pstDptx->uiSEC_PHY_Reg_Offset;

	switch (ucVsw) {
	case (uint8_t)VOLTAGE_SWING_LEVEL_0:
		ucEQ_Case = (ucPreemp + 1U);
		break;
	case (uint8_t)VOLTAGE_SWING_LEVEL_1:
		ucEQ_Case = (ucPreemp + 2U);
		break;
	case (uint8_t)VOLTAGE_SWING_LEVEL_2:
		ucEQ_Case = (ucPreemp + 3U);
		break;
	case (uint8_t)VOLTAGE_SWING_LEVEL_3:
	default:
		ucEQ_Case = (ucPreemp + 4U);
		break;
	}

	if (ucEQ_Case > 4U) {
		dptx_err("Invalid case as %u", ucEQ_Case);
		iRetVal = DPTX_RETURN_EINVAL;

		/* coverity[misra_c_2012_rule_15_1_violation : FALSE] */
		goto return_funcs;
	}

	switch (ucLane_Idx) {
	case (uint8_t)PHY_LANE_0:
		uiReg_Addr = SEC_PHY_REG0305;
		break;
	case (uint8_t)PHY_LANE_1:
		uiReg_Addr = SEC_PHY_REG0405;
		break;
	case (uint8_t)PHY_LANE_2:
		uiReg_Addr = SEC_PHY_REG0505;
		break;
	case (uint8_t)PHY_LANE_3:
	default:
		uiReg_Addr = SEC_PHY_REG0605;
		break;
	}

	switch (ucEQ_Case) {
	case 1U:
		uiReg_W_data = 0x001AU;
		break;
	case 2U:
		uiReg_W_data = 0x0016U;
		break;
	case 3U:
		uiReg_W_data = 0x0012U;
		break;
	case 4U:
	default:
		uiReg_W_data = 0x000EU;
		break;
	}

	dptx_dbg("Lane(%u), rate(%u) -> Vsw: %u, Pre-emp: %u, Case: %u, Reg[0x%x] = 0x%x", ucLane_Idx, ucLink_Rate, ucVsw, ucPreemp, ucEQ_Case, (uiReg_Offset + uiReg_Addr), uiReg_W_data);

	Dptx_Reg_Writel(pstDptx, (uiReg_Offset + uiReg_Addr), uiReg_W_data);

return_funcs:
	return iRetVal;
}


